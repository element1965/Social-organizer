const keys = [
  'heroSubtitle', 'heroSmallLine', 'heroCopyline',
  'principleTitle', 'principleText',
  'exampleTitle', 'exampleText',
  'analogTitle', 'analogText',
  'neutralTitle', 'neutralText',
  'scaleTitle', 'scaleText',
  'block6Title', 'block6Text',
  'ctaTitle', 'footer',
];

let updated = 0;
let skipped = 0;

for (const [lang, trans] of Object.entries(translations)) {
  const filePath = path.join(localesDir, `${lang}.json`);
  if (!fs.existsSync(filePath)) {
    console.log(`⚠️  ${lang}.json not found, skipping`);
    skipped++;
    continue;
  }
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  if (!data.landing) data.landing = {};
  for (const key of keys) {
    if (trans[key] !== undefined) {
      data.landing[key] = trans[key];
    }
  }
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
  console.log(`✓ ${lang}.json`);
  updated++;
}

console.log(`\nDone: ${updated} updated, ${skipped} skipped`);
